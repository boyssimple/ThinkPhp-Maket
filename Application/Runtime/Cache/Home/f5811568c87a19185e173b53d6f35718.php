客厅<br />
<br />
1、客厅一定不能太黑暗，明亮的客厅大家都会觉得眼前一亮，所以在装修客厅的时候，墙面不宜选择暗色调<br />
<br />
2、客厅的天花板如果是横梁对正，那么人就有有一种压迫感，特别是座在横梁下容易容易造成精神压力，所以最好装饰一下横梁，把它包装起来<br />
<br />
3.客厅不要放太多的玻璃装饰物，容易积累灰尘，影响空气流通，如果家里有小孩的话，也不是很安全，玻璃打碎的小渣子也不好打扫，容易割破脚<br />
<br />
<img alt="\" src="http://www.shj.cn/uploadfile/2018/23/20180323113026440.jpg" style="width: 500px; height: 375px;" /><br />
