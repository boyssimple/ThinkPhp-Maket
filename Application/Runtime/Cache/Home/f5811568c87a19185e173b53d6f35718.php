卧室</span><br />
                <div>
                    <p><span style="font-size:13px;">1、灯是双重控制，最好能安墙脚灯，在半夜不必急于找到一个开关，光学和人体感应墙脚会自动点亮，闪闪发光的耀眼的灯光也不好，有足够的照明功能就可以了。</span></p>
                    <p><span style="font-size:13px;">2、挂电视插座最好能把床头的开关，所以你不必去电视机前关掉电视，客厅是一个和相同的待遇。</span></p>
                    <p><span style="font-size:13px;">3、大衣柜书柜卧室内，如果后面的湿气很重，后面柜子防水涂料墙面，然后在背后的衣柜塑料薄膜，防止水汽侵入柜体的立场，引起柜体变形。<br />
<br />
<img alt="\" src="http://www.shj.cn/uploadfile/2018/23/20180323113528184.jpg" style="width: 500px; height: 333px;" /></span></p>
