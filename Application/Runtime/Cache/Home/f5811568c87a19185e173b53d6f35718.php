浴室<br />
<br />
1、有老人的家庭，地砖选择防滑效果好的；最好买质量好的防滑拖鞋，把垫子可以适当的补救措施。<br />
<br />
2、根据家庭装修预算，请仔细考虑做淋浴房，可以选择安装浴帘，因为便宜的淋浴房材料，不好清洁，外观也不是很好。<br />
<br />
